void solve() {

    int n;
    cin >> n;

    int digit_sum = 0;

    while(n > 0) {
        digit_sum += n % 10;
        n /= 10;
    }

    cout << digit_sum << '\n';

}

int main() {
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(cin.failbit);

    int T;
    cin >> T;

    while(T--) {
        solve();
    }
    return 0;
}